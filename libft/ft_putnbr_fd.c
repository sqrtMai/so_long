#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	long	nbr;

	nbr = n;
	if (n < 0)
	{
		write(fd, "-", 1);
		nbr *= -1;
	}
	c = nbr % 10 + '0';
	if (nbr / 10)
	{
		ft_putnbr_fd(nbr / 10, fd);
	}
	write(fd, &c, 1);
}
